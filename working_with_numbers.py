from math import *

print(3)
print(3 + (4 * 5))  # parenthesis specifies order of operation
print(10 % 3)  # % --> modulus

my_num = -5
print(my_num)
print(abs(my_num))  # absolute value
print(pow(3, 2))  # 3 raised to the power 2
print(max(4, 6))  # prints out the max number
print(min(2, 60))  # prints out the min number
print(round(3.7))  # rounds the number
print(floor(3.7))  # takes the lower number
print(ceil(3.7))  # takes the higher value
print(sqrt(36))  # square root